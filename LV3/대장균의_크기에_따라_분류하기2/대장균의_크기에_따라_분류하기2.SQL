SELECT 
    T1.ID AS ID,
    CASE 
        WHEN T1.PERCENT_DATA<=0.25 THEN 'CRITICAL'
        WHEN T1.PERCENT_DATA<=0.5 THEN 'HIGH'
        WHEN T1.PERCENT_DATA<=0.75 THEN 'MEDIUM'
        ELSE 'LOW'
    END AS COLONY_NAME
FROM (
    SELECT 
        ID,
        PERCENT_RANK() OVER (ORDER BY SIZE_OF_COLONY DESC) AS PERCENT_DATA
    FROM ECOLI_DATA
) AS T1
ORDER BY ID;