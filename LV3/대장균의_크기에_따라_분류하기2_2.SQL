WITH TMP_ECOLI_DATA AS (
    SELECT 
        ID,
        PERCENT_RANK() OVER (ORDER BY SIZE_OF_COLONY DESC) AS PER_DATA
    FROM ECOLI_DATA
)

SELECT 
    ID AS ID,
    CASE
        WHEN PER_DATA<=0.25 THEN 'CRITICAL'
        WHEN PER_DATA<=0.5 THEN 'HIGH'
        WHEN PER_DATA<=0.75 THEN 'MEDIUM'
        ELSE 'LOW'
    END AS COLONY_NAME
FROM TMP_ECOLI_DATA
ORDER BY ID ASC;